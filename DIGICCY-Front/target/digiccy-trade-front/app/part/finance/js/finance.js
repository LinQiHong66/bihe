var app=angular.module('financeApp',["ngRoute","autoActive","ngclipboard"]);
app.constant("Routes",{
    core:"/core",
    reCard:"/reCard",
    liftNow:"/liftNow",
    shiftTo:"/shiftTo",
    shiftOut:"/shiftOut",
    entrust:"/entrust",
    query:"/query",
    generalize:"/generalize",
    prize:"/prize",
    recommend:"/recommend",
    forward:"/forward",
    jiedai:"/jiedai"
});
app.config(["$routeProvider","Routes",function($routeProvider,Routes){
    $routeProvider
        .when(Routes.core,{
            templateUrl:"views/core.html",
            controller:"core"
        })
        .when(Routes.reCard,{
            templateUrl:"views/reCard.html",
            controller:"reCard"
        })
        .when(Routes.liftNow,{
            templateUrl:"views/liftNow.html",
            controller:"liftNow"
        })
        .when(Routes.shiftTo,{
            templateUrl:"views/shiftTo.html",
            controller:"shiftTo"
        })
        .when(Routes.shiftOut,{
            templateUrl:"views/shiftOut.html",
            controller:"shiftOut"
        })
        .when(Routes.entrust,{
            templateUrl:"views/entrust.html",
            controller:"entrust"
        })
        .when(Routes.query,{
            templateUrl:"views/query.html",
            controller:"query"
        })
        .when(Routes.generalize,{
            templateUrl:"views/generalize.html",
            controller:"generalize"
        })
        .when(Routes.prize,{
            templateUrl:"views/prize.html",
            controller:"prize"
        })
        .when(Routes.recommend,{
            templateUrl:"views/recommend.html",
            controller:"recommend"
        })
        .when(Routes.forward,{
            templateUrl:"views/forward.html",
            controller:"forward"
        })
        .when(Routes.jiedai,{
            templateUrl:"views/jiedai.html",
            controller:"jiedai"
        })
        .otherwise({
            redirectTo:Routes.core
        })
}]);
